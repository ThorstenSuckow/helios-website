function buildFrontmatter(meta) {
  const { title, description, slug, tags = [], keywords = [], sidebar_label, sidebar_position } = meta;
  const escapeYAML = (value) => (/[:"'\[\]{}#&*!|>@`]/.test(value) ? `"${value.replace(/"/g, '\\"')}"` : value);
  const lines = ['---'];
  lines.push(`title: ${escapeYAML(title)}`);
  if (description) lines.push(`description: ${escapeYAML(description)}`);
  if (slug) lines.push(`slug: ${slug}`);
  if (sidebar_label) lines.push(`sidebar_label: ${escapeYAML(sidebar_label)}`);
  if (sidebar_position !== undefined) lines.push(`sidebar_position: ${sidebar_position}`);
  if (tags.length) lines.push(`tags: [${tags.join(', ')}]`);
  if (keywords.length) lines.push(`keywords: [${keywords.join(', ')}]`);
  lines.push('---');
  return `${lines.join('\n')}\n\n`;
}

function ensureFrontmatter(content, meta) {
  if (content.trimStart().startsWith('---')) {
    return { frontmatter: '', body: content };
  }
  return { frontmatter: buildFrontmatter(meta), body: content.replace(/^[\n\r]+/, '') };
}

function routeFor(meta) {
  return '/docs' + (meta.slug?.startsWith('/') ? meta.slug : `/${meta.slug || ''}`);
}

function buildRouteMap(mappings) {
  const routeMap = new Map();
  for (const mapping of mappings) {
    routeMap.set(path.resolve(mapping.src), routeFor(mapping.meta));
  }
  return routeMap;
}

function repositoryNameFor(sourceFile, repoRoot) {
  return path.relative(repoRoot, sourceFile).split(path.sep)[0];
}

function rewriteLinks(content, sourceFile, repoRoot, mappings) {
  const routeMap = buildRouteMap(mappings);
  const sourceDir = path.dirname(sourceFile);
  const sourceRepoName = repositoryNameFor(sourceFile, repoRoot);
  const sourceRepoUrl = REPOSITORIES[sourceRepoName];
  const linkRegex = /(!)?\[([^\]]+)]\(([^)]+)\)/g;

  return content.replace(linkRegex, (full, bang, text, url) => {
    if (bang === '!' || url.startsWith('#') || /^(https?:)?\/\//i.test(url) || url.startsWith('/') || url.startsWith('mailto:')) {
      return full;
    }

    const normalized = url.replace(/\\/g, '/');
    const pathPart = normalized.includes('#') ? normalized.slice(0, normalized.indexOf('#')) : normalized;
    const hash = normalized.includes('#') ? normalized.slice(normalized.indexOf('#')) : '';

    if (!/\.mdx?$/i.test(pathPart)) {
      return full;
    }

    const targetAbs = path.resolve(sourceDir, pathPart);
    const normalizedTarget = path.normalize(targetAbs);

    if (routeMap.has(normalizedTarget)) {
      return `[${text}](${routeMap.get(normalizedTarget)}${hash})`;
    }

    if (sourceRepoUrl) {
      const sourceRepoRoot = path.join(repoRoot, sourceRepoName);
      const relToSourceRepo = path.relative(sourceRepoRoot, targetAbs).replace(/\\/g, '/');
      if (!relToSourceRepo.startsWith('..')) {
        return `[${text}](${sourceRepoUrl}/blob/main/${relToSourceRepo}${hash})`;
      }
    }

    return full;
  });
}

async function copyMarkdown(mapping, repoRoot, mappings) {
  const raw = await fs.readFile(mapping.src, 'utf8');
  const { frontmatter, body } = ensureFrontmatter(raw, mapping.meta);
  const rewritten = rewriteLinks(body, mapping.src, repoRoot, mappings);
  const full = `${frontmatter}${SYNC_BANNER}\n${rewritten.trimEnd()}\n`;

  await fs.mkdir(path.dirname(mapping.dest), { recursive: true });
  await fs.writeFile(mapping.dest, full, 'utf8');
  console.log(`[sync-docs] ✓ ${path.relative(repoRoot, mapping.src)} → ${path.relative(repoRoot, mapping.dest)}`);
}

